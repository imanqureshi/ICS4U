package Test;

public class Lunch {
int var;
int radius;

	public Lunch() {
	}
	public Lunch(int v, int r) {
		var = v;
		radius = r;
	}
	public void setVar(int v, int r) {
		var = v;	
	}
	public int getVar() {
		return var;
	}
	public void setRadius(int r) {
		radius = r;	
	}
	public int getRadius() {
		return radius;
	}
}
